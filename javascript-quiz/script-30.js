// Bitwise XOR
const i = 5; // 00000000000000000000000000000101 = 2^0 + 2^2 = 1 + 4 = 5
const j = 3; // 00000000000000000000000000000011 = 2^0 + 2^1 = 1 + 2 = 3

console.log("[XOR] i ^ j :", i ^ j); // 6 <- 00000000000000000000000000000110 = 2^1 + 2^2 = 2+ 4 = 6

// Bitwise XOR assignment
let x = 4;    // 00000000000000000000000000000100 = 2^2 = 4
x ^= 2;         // 00000000000000000000000000000010 = 2^1 = 2

console.log("[XOR assignment] x :", x); // 00000000000000000000000000000110 = 2^1 + 2^2 = 2 + 4 = 6
