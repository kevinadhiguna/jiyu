reliefers = ["Cano", "Bickford", "Moore"]

print("Length of {}".format(reliefers), "is", len(reliefers)) # 3
print("Type of {}".format(reliefers), "is", type(reliefers)) # <class 'list'>

# Another way to create a list
closers = list(("Bautista", "Graterol", "Estevez", "Jansen")) # <- Must be in double-round brackets

print("Length of {}".format(closers), "is", len(closers)) # 4
print("Type of {}".format(closers), "is", type(closers)) # <class 'list'>
