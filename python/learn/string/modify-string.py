alWestChampion = "Los Angeles Angels"
print("alWestChampion : {}".format(alWestChampion)) # Los Angeles Angels

alWestChampion_uppercase = alWestChampion.upper()
print("alWestChampion_uppercase : {}".format(alWestChampion_uppercase)) # LOS ANGELES ANGELS

alWestChampion_lowercase = alWestChampion.lower()
print("alWestChampion_lowercase : {}".format(alWestChampion_lowercase)) # los angeles angels

# Remove whitespace (whitespace: a space before and/or after the actual text)
alWestChampion_strip = alWestChampion.strip()
print("alWestChampion_strip : {}".format(alWestChampion_strip)) # Los Angeles Angels
