racing = ("honda", "ducati", "yamaha")

print("racing[0] : {}".format(racing[0])) # honda
print("racing[1] : {}".format(racing[1])) # ducati
print("racing[2] : {}".format(racing[2])) # yamaha

# ------------------------------------------------

# Negative indexing (starts from the last one) :
print("racing[-1] : {}".format(racing[-1])) # yamaha
print("racing[-2] : {}".format(racing[-2])) # ducati
print("racing[-3] : {}".format(racing[-3])) # honda
