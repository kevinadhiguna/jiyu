num1 = 100

num1 += 5
print("num1 += 5 -> {}".format(num1)) # num1 += 5 -> 105

num1 -= 5
print("num1 -= 5 -> {}".format(num1)) # num1 -= 5 -> 100

num1 *= 5
print("num1 *= 5 -> {}".format(num1)) # num1 *= 5 -> 500

num1 /= 5
print("num1 /= 5 -> {}".format(num1)) # num1 /= 5 -> 100.0

num1 %= 5
print("num1 %= 5 -> {}".format(num1)) # num1 %= 5 -> 0.0

num1 //= 5
print("num1 //= 5 -> {}".format(num1)) # num1 //= 5 -> 0.0

num1 **= 5
print("num1 **= 5 -> {}".format(num1)) # num1 **= 5 -> 0.0

