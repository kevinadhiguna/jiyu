# No duplicates is allowed in a dictionary!
padres = {
  "gm": "AJ Preller",
  "star": "Fernando Tatis Jr.",
  "home": "San Diego"
}

print("Padres : {}".format(padres)) # {'gm': 'AJ Preller', 'star': 'Fernando Tatis Jr.', 'home': 'San Diego'}

print("length of padres (dictionary) : {}".format(len(padres))) # 3
print("type of padres : {}".format(type(padres))) #  <class 'dict'>
