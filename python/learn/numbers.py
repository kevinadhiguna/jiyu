# 3 numeric types in Python : int, float, complex

# (1) int
var_int1 = 1
var_int2 = 9548437234
var_int3 = -684938432

# (2) float
var_float1 = 10.5
var_float2 = 3.42
var_float3 = -5.34

# (3) complex
var_complex1 = 1j
var_complex2 = 4+7j
var_complex3 = -753j

print("type of", var_int1, ": ", type(var_int1))
print("type of", var_float2, ": ", type(var_float2))
print("type of", var_complex3, ": ", type(var_complex3))
